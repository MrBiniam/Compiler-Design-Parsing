// Very simple Java program to match the parser grammar
public class SimpleTest {
    // Simple variable declaration
    private int count;
    
    // Constructor
    public SimpleTest() {
        // Simple assignment
        count = 10;
    }
    
    // Main method
    public static void main(String[] args) {
        // Local variables
        int x;
        int y;
        
        // Simple assignments
        x = 10;
        y = 20;
        
        // Conditional statement
        if (x > y) {
            x = x + 1;
        } else {
            y = y + 1;
        }
        
        // While loop
        int j;
        j = 0;
        while (j < 3) {
            j = j + 1;
        }
    }
}
